package database;

/**
 * Enumerazione che rappresenta i tipi di query che possono essere effettuate.
 */
public enum QUERY_TYPE {
	/**
	 * Rappresenta la query di tipo MIN.
	 * MIN restituisce il valore minimo di una colonna.
	 */
	MIN,

	/**
	 * Rappresenta la query di tipo MAX.
	 * MAX restituisce il valore massimo di una colonna.
	 */
	MAX
}
